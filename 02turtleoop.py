import turtle
#create a turtle 
my_turtle = turtle.Turtle()
my_turtle.shape("turtle")
my_turtle.color("red")


#commands
my_turtle.forward(100)
my_turtle.right(90)
my_turtle.forward(100)
my_turtle.left(90)
my_turtle.forward(100)
my_turtle.right(90)
my_turtle.forward(100)
my_turtle.back(100)


#end of all turtle program
turtle.done()